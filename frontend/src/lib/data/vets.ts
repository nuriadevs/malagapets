/**
 * Busca un centro veterinario por su ID
 * @param id - ID del centro veterinario
 * @returns Centro encontrado o undefined
 */
export function getVetById(id: number): Vet | undefined {
    return vetsData.find((vet) => vet.id === id);
}

/**
 * Busca centros veterinarios por tipo
 * @param type - Tipo de centro ('hospital' o 'clinic')
 * @returns Array de centros del tipo especificado
 */
export function getVetsByType(type: 'hospital' | 'clinic'): Vet[] {
    return vetsData.filter((vet) => vet.type === type);
}

/**
 * Búsqueda avanzada de centros veterinarios con múltiples criterios
 * @param query - Término de búsqueda (busca en nombre y dirección)
 * @returns Array de centros que coinciden
 */
export function searchVets(query: string): Vet[] {
    if (!query || !query.trim()) {
        return [...vetsData];
    }

    const searchTerm = query.toLowerCase().trim();

    return vetsData.filter((vet) => {
        return (
            vet.name.toLowerCase().includes(searchTerm) ||
            vet.address.toLowerCase().includes(searchTerm) ||
            vet.postalCode?.toLowerCase().includes(searchTerm)
        );
    });
}

/**
 * Filtra centros veterinarios por múltiples criterios
 * @param filters - Objeto con criterios de filtrado
 * @returns Array de centros filtrados
 */
export function filterVets(filters: {
    name?: string;
    type?: 'hospital' | 'clinic';
    city?: string;
    postalCode?: string;
}): Vet[] {
    let results = [...vetsData];

    if (filters.name) {
        const searchTerm = filters.name.toLowerCase();
        results = results.filter((vet) =>
            vet.name.toLowerCase().includes(searchTerm)
        );
    }

    if (filters.type) {
        results = results.filter((vet) => vet.type === filters.type);
    }

    if (filters.city) {
        const searchTerm = filters.city.toLowerCase();
        results = results.filter((vet) =>
            vet.address.toLowerCase().includes(searchTerm)
        );
    }

    if (filters.postalCode) {
        results = results.filter((vet) =>
            vet.postalCode?.includes(filters.postalCode!)
        );
    }

    return results;
}

/**
 * Busca centros veterinarios por ciudad (búsqueda en dirección)
 * @param city - Nombre de la ciudad
 * @returns Array de centros en esa ciudad
 */
export function getVetsByCity(city: string): Vet[] {
    const searchTerm = city.toLowerCase();
    return vetsData.filter((vet) =>
        vet.address.toLowerCase().includes(searchTerm)
    );
}

/**
 * Busca centros veterinarios por código postal
 * @param postalCode - Código postal (puede ser parcial)
 * @returns Array de centros con ese código postal
 */
export function getVetsByPostalCode(postalCode: string): Vet[] {
    return vetsData.filter((vet) => 
        vet.postalCode?.startsWith(postalCode)
    );
}

/**
 * Obtiene todos los centros veterinarios
 * @returns Array con todos los centros
 */
export function getAllVets(): Vet[] {
    return [...vetsData];
}

/**
 * Obtiene la cantidad total de centros veterinarios
 * @returns Número total de centros
 */
export function getVetsCount(): number {
    return vetsData.length;
}

/**
 * Obtiene estadísticas de centros veterinarios
 * @returns Objeto con estadísticas
 */
export function getVetsStats(): {
    total: number;
    hospitals: number;
    clinics: number;
} {
    const hospitals = vetsData.filter((vet) => vet.type === 'hospital').length;
    const clinics = vetsData.filter((vet) => vet.type === 'clinic').length;

    return {
        total: vetsData.length,
        hospitals,
        clinics,
    };
}

/**
 * Obtiene una lista única de códigos postales
 * @returns Array de códigos postales únicos ordenados
 */
export function getUniquePostalCodes(): string[] {
    return [...new Set(
        vetsData
            .map((vet) => vet.postalCode)
            .filter((code): code is Exclude<typeof code, null> => 
                code !== null && code !== undefined
            )
    )].sort();
}
